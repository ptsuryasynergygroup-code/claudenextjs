export const currentUser = users[0]

// Helper functions
export function getUserById(id: string): User | undefined {
  return users.find((u) => u.id === id)
}

export function getUsersByDepartment(departmentId: string): User[] {
  return users.filter((u) => u.departmentId === departmentId)
}

export function getUsersByBranch(branchId: string): User[] {
  return users.filter((u) => u.branchId === branchId)
}

export function getUsersByStatus(status: User['status']): User[] {
  return users.filter((u) => u.status === status)
}

export function searchUsers(query: string): User[] {
  const lowerQuery = query.toLowerCase()
  return users.filter(
    (u) =>
      u.name.toLowerCase().includes(lowerQuery) ||
      u.email.toLowerCase().includes(lowerQuery) ||
      u.employeeId.toLowerCase().includes(lowerQuery)
  )
}
